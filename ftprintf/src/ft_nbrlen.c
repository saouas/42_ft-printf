#include "../includes/libft.h"

int				ft_nbrlen(int n)
{
	int			size;

	if (n == 0)
		return (1);
	size = 0;
	if (n < 0)
		size++;
	while (n != 0)
	{
		n = n / 10;
		size++;
	}
	return (size);
}
