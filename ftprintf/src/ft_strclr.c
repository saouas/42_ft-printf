#include "../includes/libft.h"

void	ft_strclr(char *s)
{
	size_t i;
	size_t len;

	if (!s)
		return ;
	len = ft_strlen(s);
	i = 0;
	while (i < len)
	{
		s[i] = '\0';
		i++;
	}
}
