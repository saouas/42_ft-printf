#include "../includes/libft.h"

void	ft_putnbr_sizet(size_t nbr)
{
	if (nbr >= 10)
	{
		ft_putnbr(nbr / 10);
		nbr = nbr % 10;
	}
	ft_putchar(nbr + '0');
}
